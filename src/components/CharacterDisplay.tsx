const SPRITE_COLORS: Record<number, string> = {
  0: 'transparent',
  1: '', // 主色，由 color prop 决定
  2: '#333333', // 眼睛
  3: '#FF6B6B', // 鼻子/腮红
  4: '#FFB74D', // 嘴巴
  5: '#FFFFFF', // 企鹅白色肚子
}

export default function CharacterDisplay({
  animal,
  color,
  accessory,
  size = 64,
  expression = 'normal',
}: Props) {
  const canvasRef = useRef<HTMLCanvasElement>(null)

  useEffect(() => {
    const canvas = canvasRef.current
    if (!canvas) return

    const ctx = canvas.getContext('2d')
    if (!ctx) return

    const pixelSize = size / GRID
    ctx.clearRect(0, 0, size, size)

    const sprite = SPRITES[animal]?.[expression] ?? SPRITES[animal].normal

    for (let y = 0; y < GRID; y++) {
      for (let x = 0; x < GRID; x++) {
        const val = sprite[y][x]
        if (val === 0) continue

        let fillColor = SPRITE_COLORS[val]
        if (val === 1) fillColor = COLOR_MAP[color]

        ctx.fillStyle = fillColor
        ctx.fillRect(x * pixelSize, y * pixelSize, pixelSize, pixelSize)
      }
    }

    // 配饰
    if (accessory === 'hat') {
      ctx.fillStyle = '#4A4A4A'
      ctx.fillRect(4 * pixelSize, 0, 8 * pixelSize, 2 * pixelSize)
      ctx.fillRect(3 * pixelSize, 2 * pixelSize, 10 * pixelSize, pixelSize)
    } else if (accessory === 'glasses') {
      ctx.fillStyle = '#333'
      ctx.fillRect(3 * pixelSize, 4 * pixelSize, 4 * pixelSize, pixelSize)
      ctx.fillRect(9 * pixelSize, 4 * pixelSize, 4 * pixelSize, pixelSize)
      ctx.fillRect(7 * pixelSize, 4 * pixelSize, 2 * pixelSize, pixelSize)
    }
  }, [animal, color, accessory, size, expression])

  return (
    <canvas
      ref={canvasRef}
      width={size}
      height={size}
      style={{ imageRendering: 'pixelated' }}
    />
  )
}
